using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using WebAPP_Marjorie_Falcone_20240904.Data;
using WebAPP_Marjorie_Falcone_20240904.Models;

namespace WebAPP_Marjorie_Falcone_20240904.Controllers
{
    public class PostsController : Controller
    {
        private readonly WebAPP_Marjorie_Falcone_20240904Context _context;

        public PostsController(WebAPP_Marjorie_Falcone_20240904Context context)
        {
            _context = context;
        }

        // GET: Posts
        public async Task<IActionResult> Index()
        {
            var webAPP_Marjorie_Falcone_20240904Context = _context.Post.Include(p => p.User);
            return View(await webAPP_Marjorie_Falcone_20240904Context.ToListAsync());
        }

        // GET: Posts/Details/5
        public async Task<IActionResult> Details(int? id)
        {
            if (id == null || _context.Post == null)
            {
                return NotFound();
            }

            var post = await _context.Post
                .Include(p => p.User)
                .FirstOrDefaultAsync(m => m.PostId == id);
            if (post == null)
            {
                return NotFound();
            }

            return View(post);
        }

        // GET: Posts/Create
        public IActionResult Create()
        {
            ViewData["UserId"] = new SelectList(_context.User, "UserId", "UserId");
            return View();
        }

